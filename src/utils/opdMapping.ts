/**
 * Mengkonversi nama lengkap OPD ke singkatan
 * @param fullName - Nama lengkap OPD dari database
 * @returns Singkatan OPD atau nama asli jika tidak ditemukan mapping
 */
export const getOPDShortName = (fullName: string): string => {
    return OPD_MAPPING[fullName] || fullName;
};

/**
 * Mengkonversi singkatan OPD ke nama lengkap
 * @param shortName - Singkatan OPD
 * @returns Nama lengkap OPD atau singkatan asli jika tidak ditemukan mapping
 */
export const getOPDFullName = (shortName: string): string => {
    const entry = Object.entries(OPD_MAPPING).find(([full, short]) => short === shortName);
    return entry ? entry[0] : shortName;
};

/**
 * Mencari OPD berdasarkan query (support nama lengkap dan singkatan)
 * @param query - String pencarian
 * @param fullName - Nama lengkap OPD
 * @returns Boolean apakah OPD cocok dengan query
 */
export const searchOPD = (query: string, fullName: string): boolean => {
    const shortName = getOPDShortName(fullName);
    const lowerQuery = query.toLowerCase();
    return fullName.toLowerCase().includes(lowerQuery) || 
           shortName.toLowerCase().includes(lowerQuery);
};

/**
 * Mendapatkan semua OPD dengan format objek {fullName, shortName}
 * @returns Array objek OPD dengan nama lengkap dan singkatan
 */
export const getAllOPDs = () => {
    return Object.entries(OPD_MAPPING).map(([fullName, shortName]) => ({
        fullName,
        shortName
    }));
};

/**
 * Mendapatkan daftar semua nama lengkap OPD
 * @returns Array string nama lengkap OPD
 */
export const getAllOPDFullNames = (): string[] => {
    return Object.keys(OPD_MAPPING);
};

/**
 * Mendapatkan daftar semua singkatan OPD
 * @returns Array string singkatan OPD
 */
export const getAllOPDShortNames = (): string[] => {
    return Object.values(OPD_MAPPING);
};
